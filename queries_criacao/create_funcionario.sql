    email VARCHAR(256) NOt NULL UNIQUE,
    sexo VARCHAR(10) NOT NULL,
    telefone_fixo VARCHAR(15) NOT NULL,
    celular VARCHAR(15) NOT NULL,
    id_endereco INT NOT NULL REFERENCES endereco(id) ON DELETE RESTRICT
);

